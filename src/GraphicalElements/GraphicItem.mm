unprotect('GraphicItem');

module GraphicItem()
	option object;

#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
#
# Object variables
#
#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
	local
		  visible := true
		, origin := [0.0, 0.0]
		, rotation := 0.0
		, objType ::string
		;

#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
#
# get/set visible
#
#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
	#------------------------------------------------------------------------------------------------
	export
	Get_visible::static := proc(self ::GraphicItem, $)
	#------------------------------------------------------------------------------------------------
		description "Returns the visible variable.";

		return self:-visible;
	end proc:

	#------------------------------------------------------------------------------------------------
	export
	Set_visible::static := proc(self ::GraphicItem, visible ::boolean, $)
	#------------------------------------------------------------------------------------------------
		description "Updates the visible variable.";

		self:-visible := visible;

		return;
	end proc:

#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
#
# get/set origin
#
#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
	#------------------------------------------------------------------------------------------------
	export
	Get_origin::static := proc(self ::GraphicItem, $)
	#------------------------------------------------------------------------------------------------
		description "Returns the origin variable.";

		return self:-origin;
	end proc:

	#------------------------------------------------------------------------------------------------
	export
	Set_origin::static := proc(self ::GraphicItem, pt ::[numeric, numeric], $)
	#------------------------------------------------------------------------------------------------
		description "Updates the origin variable.";

		self:-origin := pt;

		return;
	end proc:

#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
#
# get/set rotation
#
#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
	#------------------------------------------------------------------------------------------------
	export
	Get_rotation::static := proc(self ::GraphicItem, $)
	#------------------------------------------------------------------------------------------------
		description "Returns the rotation variable.";

		return self:-rotation;
	end proc:

	#------------------------------------------------------------------------------------------------
	export
	Set_rotation::static := proc(self ::GraphicItem, rotation ::numeric, $)
	#------------------------------------------------------------------------------------------------
		description "Updates the rotation variable.";

		self:-rotation := rotation;

		return;
	end proc:

#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
#
# get/set objType
#
#<><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><><>
	#------------------------------------------------------------------------------------------------
	export
	Get_objType::static := proc(self ::GraphicItem, $)
	#------------------------------------------------------------------------------------------------
		description "Returns the objType variable.";

		return self:-objType;
	end proc:

	#------------------------------------------------------------------------------------------------
	export
	Set_objType::static := proc(self ::GraphicItem, objType ::string, $)
	#------------------------------------------------------------------------------------------------
		description "Updates the objType variable.";

		self:-objType := objType;

		return;
	end proc:

end module:

